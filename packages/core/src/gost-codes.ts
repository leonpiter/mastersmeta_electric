/** Полный справочник буквенных кодов (ГОСТ 2.710 / Табл. А1). */
export const GOST_LETTER_CODES: LetterCode[] = TABLE.map(([code, name]) => ({
  code,
  name,
  firstLetter: code[0],
}));

/** Множество кодов — для быстрой проверки инварианта `componentCode ∈ коды`. */
export const GOST_CODE_SET: ReadonlySet<string> = new Set(GOST_LETTER_CODES.map((c) => c.code));

/** Найти запись по точному коду. */
export function findLetterCode(code: string): LetterCode | undefined {
  return GOST_LETTER_CODES.find((c) => c.code === code);
}

/** Сгруппировать коды по первой букве (для справочника/навигации), буквы по алфавиту. */
export function groupByFirstLetter(
  codes: LetterCode[] = GOST_LETTER_CODES,
): Map<string, LetterCode[]> {
  const m = new Map<string, LetterCode[]>();
  for (const c of codes) {
    const arr = m.get(c.firstLetter);
    if (arr) arr.push(c);
    else m.set(c.firstLetter, [c]);
  }
  return new Map([...m.entries()].sort(([a], [b]) => a.localeCompare(b)));
}
